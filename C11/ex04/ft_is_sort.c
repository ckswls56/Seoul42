int	ft_is_sort(int *tab, int length, int (*f)(int, int))
{
	int	cmp[2];

	cmp[0] = 0;
	while (1 < length--)
	{
		cmp[1] = f(tab[length], tab[length - 1]);
		if ((cmp[0] > 0 && cmp[1] < 0) || (cmp[0] < 0 && cmp[1] > 0))
			return (0);
		if (cmp[1] != 0)
			cmp[0] = cmp[1];
	}
	return (1);
}
